
public class Wage {
	String source;
	double amount;
	String Month;
	
	
	public Wage(String source, double amount, String month) {
		super();
		this.source = source;
		this.amount = amount;
		Month = month;
	}

	public Wage(String source, double amount) {
		super();
		this.source = source;
		this.amount = amount;
	}
	
}
